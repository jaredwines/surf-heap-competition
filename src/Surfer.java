public class Surfer
{
    public int    score;
    public String name;
    
    /**
     * Default constructor
     * 
     * @param score The surf's score
     * @param name The surf's name
     */
    public Surfer(int score, String name)
    {
        this.name = name;
        this.score = score;
        
    }
    
}
